TEST return_program(void) {
  Function f;
  function_init(&f, "=(none)");

  AstNode num;
  ast_number_init(&num, 42);

  AstNode ret;
  ast_return_init(&ret, &num);

  list_push(&f.code.as_block.stmts, &ret);

  unsigned char chunk[1024];
  compile(&f, chunk);

  ASSERT_MEM_EQ(luac_out, chunk, luac_out_len);
  PASS();
}

